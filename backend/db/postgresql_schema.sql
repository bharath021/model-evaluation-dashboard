-- PostgreSQL deployment schema for the model evaluation dashboard.
-- SQLite local development uses the same logical tables in app/db/repository.py.

CREATE TABLE IF NOT EXISTS eval_runs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    prompt TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS model_responses (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id BIGINT NOT NULL REFERENCES eval_runs(id) ON DELETE CASCADE,
    model_name TEXT NOT NULL,
    response_text TEXT NOT NULL,
    score_relevance SMALLINT NOT NULL CHECK (score_relevance BETWEEN 0 AND 10),
    score_clarity SMALLINT NOT NULL CHECK (score_clarity BETWEEN 0 AND 10),
    score_length SMALLINT NOT NULL CHECK (score_length BETWEEN 0 AND 10),
    response_time_ms INTEGER NOT NULL CHECK (response_time_ms >= 0),
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_eval_runs_created_at
ON eval_runs(created_at DESC);

CREATE INDEX IF NOT EXISTS idx_model_responses_run_id
ON model_responses(run_id);

CREATE INDEX IF NOT EXISTS idx_model_responses_model_created_at
ON model_responses(model_name, created_at DESC);
